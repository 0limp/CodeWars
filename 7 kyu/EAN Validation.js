// Проверка штрихкода EAN

// Многие товары имеют международный номер артикула — EAN (ранее известный как «Европейский номер артикула»).
// EAN — это штрихкод из 13 цифр, состоящий из 12 основных цифр и одной контрольной цифры (формат EAN-8 в этой
// задаче не рассматривается).

// Контрольная цифра вычисляется следующим образом (на основе первых 12 цифр):
// - Цифры на нечётных позициях (1-я, 3-я, 5-я и т.д.) умножаются на 1.
// - Цифры на чётных позициях (2-я, 4-я, 6-я и т.д.) умножаются на 3.
// - Полученные значения складываются.

// Если сумма делится на 10, то контрольная цифра равна 0. В противном случае она вычисляется по формуле:
// ! контрольная_цифра = 10 − (сумма % 10)

// 📌 Пример: вычислим контрольную цифру для кода 400330101839 (12 цифр):
// 4×1 + 0×3 + 0×1 + 3×3 + 3×1 + 0×3 + 1×1 + 0×3 + 1×1 + 8×3 + 3×1 + 9×3 = 4 + 0 + 0 + 9 + 3 + 0 + 1 + 0 + 1 + 24 + 3 + 27 = 72
// контрольная_цифра = 10 − (72 % 10) = 8
// Итоговый EAN-код: 400330101839[8] (12 цифр + контрольная цифра).

// 🎯 Ваша задача Проверить, является ли заданный EAN-код корректным. Верните True (или true), если код действителен,
// иначе — False (или false).

// 📌 Условие Можно считать, что входной код синтаксически корректен: он состоит только из цифр и имеет ровно 13 символов.
// 📚 Примеры (Примеры кода не приведены, но подразумеваются в задаче.)

const validateEAN = (code) => {
  const checkSum = String(code)
    .slice(0, 12)
    .split("")
    .map((e, i) => (i % 2 === 0 ? Number(e) * 1 : Number(e) * 3))
    .reduce((acc, iter) => acc + iter, 0);

  const checkNum = checkSum % 10 === 0 ? 0 : 10 - (checkSum % 10);

  return Number(String(code)[12]) === checkNum;
};
